product = input()

if (product == 'apple' or product == 'banana' or product == 'kiwi' or product == 'cherry' or
    product == 'lemon' or product == 'grapes'):
    print('fruit')
elif product == 'tomato' or product == 'cucumber' or product == 'pepper' or product == 'carrot':
    print('vegetable')
else:
    print('unknown')
